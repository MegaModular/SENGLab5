package com.example.lab5;

public class MathUtil {
    public static double abs(double value) {
        return Math.abs(value);
    }

    public static double add(double val1, double val2) {
        return val1 + val2;
    }

    public static double sub(double val1, double val2) {
        return val1 - val2;
    }
}
